public class EvenOdd100 {
    public static void main(String[] args) {
        System.out.println("check number is even odd");
        for (int i =0; i<=100; i++){
            if (i%2==0){
                System.out.println(i+ " is a even number");
            }
            else {
                System.out.println(i+ " is a odd number");
            }
        }
    }
}
