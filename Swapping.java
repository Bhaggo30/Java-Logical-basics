public class Swapping {
    public static void main(String[] args) {
        int a, b, temp;
        a=10;
        b=20;
        System.out.println("before swapping of the value of a and b is "+a+ " "+b);
        temp =a;
        a =b;
        b=temp;
        System.out.println("after swapping of two value is a and b is "+a+ " "+b);
    }
}
